Die dreifaltige Göttin
Hera-goddess-icon-1.jpg.webp
Dieses Hexen-Zeichen stellt die dreifaltige Göttin dar: Die Jungfrau, die Mutter und das Alte Weib.

Die dreifaltige Göttin ist so antik, dass man die Anzahl der Namen, die ihr gegeben wurden, nicht mehr zählen kann, aber sie wird am häufigsten als Hekate, die griechische Göttin der Magie, angesehen.
Außerdem repräsentiert dieses Hexen-Zeichen die Mondphasen, die für Hexen von großer Bedeutung sind.

Die meisten Rituale und wichtigen Ereignisse für die Ausübung der Hexerei während des Vollmondes oder der wechselnden Mondphasen stattfinden.
Da dieses Hexen-Zeichen auch von weiblicher Polarität ist, ist es eine wunderbare Darstellung des weiblichen Reifeprozesses.
Bei allen Ritualen, die mit dem Mond zu tun haben, wird dieses Symbol ein mächtiger Begleiter sein.
Es ist auch nützlich, wenn wir uns mit unserer Weiblichkeit (göttliches Weibchen) verbinden wollen, und kann als Schutz und Macht-Symbol verwendet werden.

Der gehörnte Gott
The-horned-god.jpg.webp

Dieses Hexen-Zeichen steht für den gehörnten Gott und ist das Gegenstück zur dreifaltigen Göttin.
Der gehörnte Gott ist der Gott der Fruchtbarkeit und der Vegetation, wo die alten Zivilisationen viele gehörnte Götter verehrten.
Die häufigsten Götter, die in Wicca durch dieses Symbol dargestellt werden, sind der griechische Gott Pan und der keltische Gott Cernunnos.

Als Hexen-Zeichen birgt es männliche Energie und repräsentiert das göttliche Männliche.
Der gehörnte Gott ist der Gott der Hexerei, aber er ist auch der Gott der Jagd, der Natur, dermenschlichen Instinkte und der wilden Tiere.
Die Verwendung dieses Hexen-Zeichens in Ritualen kann uns helfen, Kraft zu gewinnen, und eine Verbindung mit dem gehörnten Gott herzustellen.
Das Herbeirufen des gehörnten Gottes bei Ritualen, die in der Natur stattfinden, ist ebenfalls äußerst wirksam, wenn wir Beratung brauchen.


Das Pentagramm
pentagram-star-wicca.jpg.webp

Dieses Hexen-Zeichen ist eine Darstellung des Glaubens und der 5 Elemente: Luft, Feuer, Erde, Wasser und Äther.
In der Wicca-Religion ist dieses Symbol nicht böse oder finster und wird zum Schutz verwendet.
Seine Hauptaufgabe ist die Abwehr des Bösen, und das wird dadurch erreicht, dass dieses Hexen-Zeichen als Anhänger getragen oder an Türen und Fenstern aufgehängt wird.

Die Form des Pentagramms findet sich in vielen Lebensformen, und die Zahl 5, die es widerspiegelt,ist die Darstellung des Lebens und seiner Zyklen.

Durch das Tragen des Pentagramms und die Verwendung des Symbols in unseren Ritualen und im Alltag werden wir auch stärker mit der Natur und den Elementen verbunden.

Da dieses Hexen-Zeichen den Wicca-Glauben repräsentiert, wird es auch von seinen Anhängern benutzt, um sich gegenseitig zu identifizieren.
Das Pentagramm wird auch während magischer Rituale auf den Boden oder eine andere Oberfläche gezeichnet, um negative Energien zu bannen oder jene zu beschwören, die den magischen Zauber unterstützen.


Das Hexagramm

Hexagram-wicca.jpg.webp

Dieses Hexen-Zeichen ist sehr wichtig in der Hexerei, wo der sechszackige Stern die Vereinigung von Mann und Frau darstellt.
Neben der Vereinigung von Mann und Frau steht sie auch für die Vereinigung von Feuer und Wasser, Himmel und Erde sowie Licht und Dunkelheit.
Das Hexagramm enthält in seiner Form planetarische Energien und wird als solches am häufigsten inkosmischen Ritualen verwendet.
Ein weiterer Name des Hexagramms ist ebenfalls der Davidstern, und er steht ebenfalls für den vollkommenen Menschen.

Wenn man dieses Hexen-Zeichen trägt, kann man auch Geister und spirituelle Kräfte beschwören.
Dieses uralte Symbol steht auch für die 7 alten Planeten: Saturn, Jupiter, Mars, Venus, Merkur, wobei Sonne und Mond in alten Zeiten ebenfalls als Planeten betrachtet wurden.
Wegen seiner Verbindung zu Himmelskörpern ist es kein Wunder, dass er ein großes Potenzial für planetarische Rituale und für die Verbannung und Beschwörung ihrer Energien birgt.


Das Wicca Jahreskreis

wheel-of-the-year.jpg.webp

Dieses Hexen-Zeichen wird auch als Sonnenrad bezeichnet, weil das Rad Teil eines Wagens ist, den eine Sonnengottheit fahren würde.
Die alten Zivilisationen haben stets die Sonne und den Mond angebetet, wobei die Sonne als Symbol eine große magische Kraft war.
Dieses Jahreskreis steht für die Sonne, aber auch für die Jahreszeiten und Feste.
Die Feiertage, die von den Wicca-Praktizierenden gefeiert werden, werden durch dieses Rad bestimmt, wobei jede Spitze des Rades einen Sabbat darstellt.


Ein Hexen Sabbat ist die Zeit, in der Menschen zusammenkommen, um Hexerei und andere Rituale zu praktizieren.


Die Wicca-Praktizierenden haben auch 8 große und 13 kleinere Feiertage und dürfen auch andere Feste feiern.
Moderne Hexen und Wicca-Praktizierende sehen große Bedeutung darin, die Natur und ihre Zyklen zu feiern, und diese Feste haben sowohl jahreszeitliche als auch solare Aspekte.

Die acht wichtigsten Sabbate werden ebenfalls an einem bestimmten Datum gefeiert:
