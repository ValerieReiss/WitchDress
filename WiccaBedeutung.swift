Beltane: 1. Mai
Mabon: 1. September
Lammas: 1. August
Litha: 21. Juni


Das Element Luft
Air-element-wicca.jpg.webp
Dieses Hexen-Zeichen repräsentiert das Reich jenseits von uns, die Geisterwelt und das Element, das zur Erhaltung des Lebens notwendig ist.

Das Symbol der Luft beschreibt eindeutig eine ganz andere Welt jenseits der materiellen, in der wir leben, und erhebt so unseren Geist zu neuen Möglichkeiten.
Luft ist überall um uns herum und bringt Veränderungen für die Menschen und die Erde gleichermaßen.
Sie kann nicht an einen Ort gebunden werden, und sie ist immer und ewig präsent und umgibt alles, was lebt und spendet gleichzeitig Leben.
Im traditionellen Wicca steht die Luft für Logik, Intellekt, Kommunikation und Osten.
Dieses Hexen-Zeichen ist äußerst hilfreich, wenn wir Veränderungen in unser Leben bringen wollen und wenn wir uns konzentrieren müssen.
Rituale, die unter dem blauen Himmel durchgeführt werden, verleihen auch diesem Hexen-Zeichen zusätzliche Kraft und setzen sein Potenzial frei.


Das Element Feuer
Fire-element-wicca.jpg.webp
Dieses Hexen-Zeichen steht für die mächtige, dramatische und zerstörerische Flamme.
Das Feuer erwacht mit der Hitze zum Leben, und wenn es seine Aufgabe erfüllt hat, löst es sich auf und hinterlässt die Spuren seines Einflusses.

Feuer ist weder gut noch böse, es wärmt unseren Körper und dient uns als Werkzeug für ein leichteres Leben, als integraler Bestandteil von Ritualen, aber auch als Waffe der Zerstörung.
Es nährt uns genauso wie andere Elemente, aber nur, wenn wir mit ihm gütig und weise umgehen.
Im traditionellen Wicca steht das Feuer für Ehrgeiz, Aktivität, Begierde, Mut, Verwandlung und Süden.
Feuer ist unerlässlich für Kerzenmagie und Rituale, die schlechte Energie verbannen.
Bei Ritualen wird das Feuer auch dazu verwendet, den Raum von schlechten Energien zu reinigen und die Umgebung zu erhellen, damit kraftvolle und positive Energien eintreten können.


Das Element Erde
Earth-element-wicca.jpg.webp

Dieses Hexen-Zeichen ist das repräsentiert unserer Heimat, unserer Mutter Erde und des Lebensprozesses.
Nach der Geburt wird uns das Geschenk des Lebens gegeben, wonach wir sterben und unser Körper als Basis für einen Neuanfang dient.
Die Menschheit ist aus der Erde gemacht und wird so wieder ein Teil von ihr.
Im traditionellen Wicca repräsentiert das Element Erde die Göttin, das Pentagramm und die Norden.

Dieses Hexen-Zeichen steht auch für Wachstum, Stärke und Stabilität.
Bei der Verwendung in magischen Ritualen unterstützt die Erde jede Art von Entwicklung.
Rituale, die in der Natur durchgeführt werden, werden dieses Hexen-Zeichen ebenfalls sein volles Potenzial entfalten lassen.

Das Element Wasser
Water-element-wicca.jpg.webp

Dieses Hexen-Zeichen repräsentiert das lebensspendende Wasser, die Essenz des Lebens.
Wasser ist ein lebenswichtiger Teil von uns, und ohne Wasser wäre kein Leben möglich.
Zusammen mit den anderen Elementen bildet sie unsere Heimat, Mutter Erde, und ist in jeder Hinsicht göttlich.
Im traditionellen Wicca ist das Wasser ein Symbol für Liebe, Träume, Reinheit, Wünsche, Emotionensowie für den Westen.
Wasser ist auch mit der Göttin verwandt und repräsentiert Weiblichkeit.
Dieses Hexen-Zeichen wird meist bei Reinigung, Heilung, und Klärungsritualen verwendet.

Ebenso wie die anderen Elemente ist Wasser in jedem Zauberspruch und Ritual vorhanden, und Rituale bestehen immer aus allen Grundelementen.
Die Spiralgöttin

https://mystischerrabe.de/wp-content/uploads/2020/11/Spiral-goddess-wicca-545x1536.jpg

Dieses Hexen-Zeichen steht für weibliche Energie und Macht.
Die Spiralgöttin ist in der Wicca-Tradition eine Ikone, aber sie entstand ursprünglich als eine Bewegung zur Verehrung der Weiblichkeit.
Alte Zivilisationen haben schon immer Ikonen der Fruchtbarkeit, der Geschlechtlichkeit und der weiblichen Aspekte verehrt, so dass die Spiralgöttin diese Praktiken zurückbringt.


Dieses Hexen-Zeichen ist auch sehr wichtig für die Praktizierenden des Dianischen Wicca (Dem Hexenzirkel, der hauptsächlich aus Frauen besteht und weibliche Göttinnen anbetet).
Während einige Wicca-Praktizierende glauben, dass es viele Göttinnen gibt, die sie auch verehren, glauben die Dianischen Wicca-Praktizierenden, dass es nur eine einzige Göttin gibt.
Außerdem hat diese Göttin in der Mitte ihres Bauches auch eine Spirale, die Kreativität, Weiblichkeit, das Sakralchakra und den Menstruationszyklus repräsentiert.
Dieses Hexen-Zeichen ist für alle Hexer wichtig, da weise Frauen und die Hebammen als die ersten aller Hexen angesehen werden.

Hekates Rad
hecates-circle-wicca.jpg.webp
Dieses Hexen-Zeichen stellt die griechische Göttin Hekate dar.
Hekate war die Göttin der Magie, der Scheidewege, der Hexerei, der Geister, der Kräuter, der Dunkelheit und des Lichts.

Sie ist auch die dreifaltige Göttin, und durch die Kultur und Kunst wurde sie meist mit drei Köpfen dargestellt.
Wegen ihrer Verknüpfungen wird sie oft als die Mutter aller Hexen bezeichnet.
Schon seit der Antike hatte sie eine große Anhängerschaft von Hexen, die in Thessalien lebten, was sie zu einem der wichtigsten Darsteller der Hexerei macht.
Dieses mächtige Hexen-Zeichen steht für die Wiedergeburt mit einem Schlangenlabyrinth, das die zentrale Spirale umgibt.
Auch die Praktizierenden des Wicca tragen dieses Hexen-Zeichen als Darstellung ihres Glaubens und rufen Hekate mit diesem Symbol in Ritualen auf.
Die Triskele
Triskelion-wicca.jpg.webp

Dieses Hexen-Zeichen ist auch mit der dreifaltigen Göttin, aber auch mit dem Zyklus des Lebens verbunden: Geburt, Leben, Tod.
Das Triskele Symbol wird seit der Antike verwendet, und im Allgemeinen ist es ein Symbol des Fortschritts und hat in keltischen Traditionen einen Bezug zu den Lebenszyklen.

In den keltischen Traditionen repräsentierte es nicht nur Geburt, Leben und Tod, sondern auch Verstand, Körper, Geist sowie Vergangenheit, Gegenwart und Zukunft.
Dieses Symbol steht auch für die drei keltischen Welten: Die geistige Welt, die gegenwärtige Welt und die himmlische Welt.
Auch wenn dieses Symbol meist mit Irland und Sizilien assoziiert wird, hat es alte heidnische Wurzeln und damit Bedeutung für Wicca.
Das Grab, das mit diesem Symbol namens Newgrange geschmückt ist, ist älter als selbst die Ägyptischen Pyramiden und Stonehange.
Dieses Symbol wird auch meistens von Wicca-Praktikern verwendet, die keltische Wurzeln haben, so dass sie normalerweise ein Sigillen (Zeichen, die Macht haben) mit diesem Symbol anfertigen.


Die Triqueta
Triquetra-wicca.jpg.webp
Dieses Hexen-Zeichen hat im Christentum eine ganz andere Bedeutung als im Wicca.

Im Christentum stellt dieses Symbol die heilige Dreieinigkeit dar: Vater, Sohn und Geist.
Andererseits ist dies in der Wicca-Religion ein Hexen-Zeichen für die drei Reiche: Erde, Meer und Himmel.
In Wicca repräsentiert dieses Hexen-Zeichen auch die drei Aspekte der dreifaltigen Göttin.
Dieses wunderbare Symbol spiegelt perfekt die drei Gesichter der Göttin wider und feiert damit auch das göttliche Weibliche.
Das Interessante an diesem Symbol ist, dass es noch älter als das Christentum ist und dass es auch in keltischen und nordischen Traditionen präsent ist.
In der keltischen Tradition ist es ein Symbol der Stärke und in der nordischen ein Symbol des Gottes Odin.

Hexen-Runen, die in Wicca eingesetzt werden
Witch-book-magic-wicca-1280x860.jpg.webp
Neben Hexen-Zeichen sind Hexen-Runen ein wesentliches magisches Hilfsmittel beim Zaubern.
Sie unterscheiden sich von Zeichen, weil sie in Form von Runen existieren, aber sie können auch verwendet werden, ohne auf eine Rune gezeichnet oder geschnitzt zu werden.

Ihre Symbole bergen ein großes magisches Potential und sie haben die Macht, bestimmte Bereiche unseres Lebens zu verbessern.
Jedes Symbol hat die Macht, bestimmte Bereiche in unserem Leben zu verbessern, und es bringt Hoffnung in die Dinge, die wir mit Hilfe von Zaubersprüchen in unserem Leben manifestieren wollen.

Diese magischen Runen werden nicht nur beim Zaubern, sondern auch bei der Heilung, bei Ritualen und bei der Wahrsagerei verwendet.

Es handelt sich dabei um eine Gruppe von einfachen Symbolen, die eine sehr mächtige Natur haben und die direkt mit dem Zauberwirker sprechen, während er sie benutzt.
Selbst wenn man diese Runen nur beobachtet, kann man wissen, was ihr Spezialgebiet ist und in welcher Weise sie verwendet werden können.
Auch wenn eine Person das Gefühl hat, dass diese Runen einen anderen Zweck haben als den beschriebenen, können sie immer noch für dieses Gebiet verwendet werden.
Im Allgemeinen sind dies die häufigsten Runen die folgenden, und ihre Kraft kann unserem Leben definitiv zugute kommen.

